from math import *

my_num = -5
print("The number is", my_num)
print("The String Number is " + str(my_num))
print("Absolute value is ", abs(my_num))
print("The square is ", pow(my_num, 2))
print("The larger number between 4 and 6 is ", max(4, 6))
print("Rounded up number of 3.142 is ", round(3.142))
print("Flooring of pie is ", floor(3.142))
print("Ceil of pie is ", ceil(3.142))
print("Square root of pie is ", sqrt(3.142))